    private String calculatepriority(LocalDateTime dueDate,boolean urgentdetect){
        if(urgentdetect){
            return "HIGH";
        }
        if(dueDate!=null){
            // compute full-day difference using dates (more accurate than Duration.toDays())
            long days = java.time.temporal.ChronoUnit.DAYS.between(java.time.LocalDate.now(), dueDate.toLocalDate());
            if(days <= 2) return "HIGH";
            if(days <= 5) return "MEDIUM";
            return "LOW";
        }
        return "MEDIUM";
    }
    public Task updateTask(Long taskId, TaskEditDto dto, Authentication auth) {
        Task task = taskRepository.findById(taskId)
                .orElseThrow(() -> new ResourceNotFoundException("Task not found"));

        // Optional: authorization check (mentor/admin only or owner)
        // if (!authorized) throw new AccessDeniedException(...);

        if (dto.getTitle() != null) task.setTitle(dto.getTitle().trim());
        if (dto.getAssignedTo() != null) task.setAssignedTo(dto.getAssignedTo().trim());
        if (dto.getDueDate() != null) {
            task.setDueDate(dto.getDueDate());
            task.setPriority(calculatepriority(dto.getDueDate(), task.isAiSuggestedDeadline()));
        }
        if (dto.getPriority() != null) {
            task.setPriority(dto.getPriority().toUpperCase());
        } else {
            task.setPriority(calculatepriority(task.getDueDate(), task.isAiSuggestedDeadline()));
        }
        if (dto.getUrgent() != null) {
            task.setAiSuggestedDeadline(dto.getUrgent());
            task.setPriority(calculatepriority(task.getDueDate(), dto.getUrgent()));
        } // or set separate field
        if (dto.getResources() != null) task.setResources(dto.getResources());
        return taskRepository.save(task);
    }
    public List<TaskAlertDto> getDeadlineAlerts(String email){
        String username=userservice.getUserNameByEmail(email);
        List<String> statuses =
                List.of("NOT_STARTED","IN_PROGRESS");

        List<Task> tasks =
                taskRepository.findByAssignedToAndStatusIn(username,statuses);

        LocalDateTime now = LocalDateTime.now();

        return tasks.stream()
                .filter(task -> task.getDueDate() != null)
                .map(task -> {

                    long hours =
                            Duration.between(now, task.getDueDate()).toHours();

                    String alertType;
                    if(hours < 0){
                        return null;
                    }
                    else if(hours <= 24){
                        alertType = "URGENT";
                    }
                    else if(hours <= 48){
                        alertType = "WARNING";
                    }
                    else{
                        return null;
                    }

                    return new TaskAlertDto(
                            task.getTitle(),
                            task.getPriority(),
                            task.getDueDate(),
                            hours,
                            alertType
                    );

                })
                .filter(Objects::nonNull)
                .toList();
    }
    public Task cancelTask(Long TaskId,Authentication authentication){
        Task task=taskRepository.findById(TaskId).orElseThrow(() -> new ResourceNotFoundException("Task Not Found"));
        String email=authentication.getName();
        Long userId=userservice.getUserIdByEmail(email);
        Meeting meeting=task.getMom().getMeeting();
        if(!meeting.getMentorid().equals(userId)){
            throw new RuntimeException("Only Mentor Can Cancel The Task");
        }
        if(!task.isApproved()){
            throw new RuntimeException("Only Approved Tasks Can Be Cancelled");
        }
        if("CANCELLED".equalsIgnoreCase(task.getStatus())){
            throw new RuntimeException("Task Already Cancelled");
        }
        task.setStatus("CANCELLED");
        return taskRepository.save(task);
    }
