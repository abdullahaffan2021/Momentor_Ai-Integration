    private LocalDateTime parseAiDeadline(String text) {
        if (text == null || text.isBlank()) return null;
        String s = text.trim().toLowerCase();
        LocalDateTime now = LocalDateTime.now();

        // helper to set business hour
        java.util.function.Function<LocalDateTime, LocalDateTime> atFivePm =
                dt -> dt.withHour(17).withMinute(0).withSecond(0).withNano(0);

        // direct keywords
        if (s.contains("today")) return atFivePm.apply(now);
        if (s.contains("tomorrow")) return atFivePm.apply(now.plusDays(1));

        // in N days/weeks/months / within N days
        java.util.regex.Pattern p;
        java.util.regex.Matcher m;

        p = java.util.regex.Pattern.compile("in\\s+(\\d+)\\s+days?");
        m = p.matcher(s);
        if (m.find()) {
            int n = Integer.parseInt(m.group(1));
            return atFivePm.apply(now.plusDays(n));
        }

        p = java.util.regex.Pattern.compile("in\\s+(\\d+)\\s+weeks?");
        m = p.matcher(s);
        if (m.find()) {
            int n = Integer.parseInt(m.group(1));
            return atFivePm.apply(now.plusWeeks(n));
        }

        p = java.util.regex.Pattern.compile("in\\s+(\\d+)\\s+months?");
        m = p.matcher(s);
        if (m.find()) {
            int n = Integer.parseInt(m.group(1));
            return atFivePm.apply(now.plusMonths(n));
        }

        p = java.util.regex.Pattern.compile("within\\s+(\\d+)\\s+days?");
        m = p.matcher(s);
        if (m.find()) {
            int n = Integer.parseInt(m.group(1));
            return atFivePm.apply(now.plusDays(n));
        }

        // next week / next month
        if (s.contains("next week")) return atFivePm.apply(now.plusWeeks(1));
        if (s.contains("next month")) {
            LocalDateTime candidate = now.plusMonths(1);
            int day = Math.min(now.getDayOfMonth(), candidate.toLocalDate().lengthOfMonth());
            return atFivePm.apply(candidate.withDayOfMonth(day));
        }

        // start / first of next month
        if (s.contains("first of next month") || s.contains("start of next month")) {
            LocalDateTime nm = now.plusMonths(1).withDayOfMonth(1);
            return atFivePm.apply(nm);
        }

        // end of month / end of next month
        if (s.contains("end of next month")) {
            LocalDateTime nm = now.plusMonths(1);
            LocalDate last = nm.toLocalDate().withDayOfMonth(nm.toLocalDate().lengthOfMonth());
            return atFivePm.apply(last.atStartOfDay());
        }
        if (s.contains("end of month") || s.contains("end of this month")) {
            LocalDate last = now.toLocalDate().withDayOfMonth(now.toLocalDate().lengthOfMonth());
            return atFivePm.apply(last.atStartOfDay());
        }

        // "by <day>" or "next <day>" or plain day name
        for (java.time.DayOfWeek d : java.time.DayOfWeek.values()) {
            String dayName = d.name().toLowerCase(); // MONDAY, etc.
            if (s.contains("next " + dayName)) {
                // next occurrence of that weekday (skip this week)
                int daysUntil = (d.getValue() - now.getDayOfWeek().getValue() + 7) % 7;
                if (daysUntil == 0) daysUntil = 7;
                return atFivePm.apply(now.plusDays(daysUntil + 7));
            }
            // "by friday" or plain "friday" -> next occurrence of the weekday (if today is that day, choose next week)
            if (s.matches(".*\\bby\\s+" + dayName + "\\b.*") || (s.contains(dayName) && !s.contains("next"))) {
                int daysUntil = (d.getValue() - now.getDayOfWeek().getValue() + 7) % 7;
                if (daysUntil == 0) daysUntil = 7;
                return atFivePm.apply(now.plusDays(daysUntil));
            }
        }

        // Try common date formats: ISO first, then "d MMMM", "MMMM d", "d/MM/yyyy", "d-M-yyyy"
        try {
            java.time.LocalDate ld = java.time.LocalDate.parse(text.trim()); // ISO yyyy-MM-dd
            return atFivePm.apply(ld.atStartOfDay());
        } catch (Exception ignored) {}

        java.time.format.DateTimeFormatter[] fmts = new java.time.format.DateTimeFormatter[] {
                java.time.format.DateTimeFormatter.ofPattern("d MMMM yyyy"),
                java.time.format.DateTimeFormatter.ofPattern("d MMMM"),
                java.time.format.DateTimeFormatter.ofPattern("MMMM d yyyy"),
                java.time.format.DateTimeFormatter.ofPattern("MMMM d"),
                java.time.format.DateTimeFormatter.ofPattern("d/MM/yyyy"),
                java.time.format.DateTimeFormatter.ofPattern("d-M-yyyy")
        };
        for (java.time.format.DateTimeFormatter fmt : fmts) {
            try {
                java.time.LocalDate ld = java.time.LocalDate.parse(text.trim(), fmt);
                // if year missing, assume this year or next if past
                if (!fmt.toString().contains("yyyy")) {
                    if (ld.isBefore(now.toLocalDate())) ld = ld.plusYears(1);
                }
                return atFivePm.apply(ld.atStartOfDay());
            } catch (Exception ignored) {}
        }

        // fallback: can't parse
        return null;
    }
